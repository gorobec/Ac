package lesson2;

public class Calc {
	public int add(int a, int b){
		int result = a + b;
		return result;
	}
	public int minus(int a, int b){
		int result = a - b;
		return result;
	}
	public int del(int a, int b){
		int result = a / b;
		return result;
	}
	public int ymnog(int a, int b){
		int result = a * b;
		return result;
	}
}
