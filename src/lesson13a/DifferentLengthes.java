package lesson13a;

public class DifferentLengthes {
	
	private int length1;
	private int length2;
	private int length3;
	public DifferentLengthes(int length1, int length2, int length3) {
		super();
		this.length1 = length1;
		this.length2 = length2;
		this.length3 = length3;
	}
	public int getLength1() {
		return length1;
	}
	public int getLength2() {
		return length2;
	}
	public int getLength3() {
		return length3;
	}
	
	
}
